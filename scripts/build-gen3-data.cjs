const fs = require("node:fs");
const path = require("node:path");

const showdownDexPath =
  process.env.SHOWDOWN_DEX_PATH || "pokemon-showdown/dist/sim/dex";
const { Dex } = require(showdownDexPath);
const showdownPackage = require(path.join(path.dirname(require.resolve(showdownDexPath)), "..", "..", "package.json"));

const dex = Dex.mod("gen3");
const outputPath = path.resolve(__dirname, "..", "gen3-data.js");

function byName(a, b) {
  return a.name.localeCompare(b.name);
}

function publicEntry(entry) {
  return entry.exists && entry.num > 0 && entry.gen <= 3 && !entry.isNonstandard;
}

function speciesEntry(species) {
  return {
    id: species.id,
    name: species.name,
    num: species.num,
    types: species.types,
    baseStats: species.baseStats,
    abilities: Object.values(species.abilities).filter(Boolean),
  };
}

const species = dex.species
  .all()
  .filter(publicEntry)
  .map(speciesEntry)
  .sort(byName);

const moves = dex.moves
  .all()
  .filter(publicEntry)
  .map((move) => ({ id: move.id, name: move.name, num: move.num }))
  .sort(byName);

const items = dex.items
  .all()
  .filter(publicEntry)
  .map((item) => ({ id: item.id, name: item.name, num: item.num }))
  .sort(byName);

const abilities = dex.abilities
  .all()
  .filter((ability) => publicEntry(ability) && ability.id !== "noability")
  .map((ability) => ({ id: ability.id, name: ability.name, num: ability.num }))
  .sort(byName);

const data = {
  source: `pokemon-showdown@${showdownPackage.version} Dex.mod('gen3')`,
  species,
  moves,
  items,
  abilities,
};

fs.writeFileSync(
  outputPath,
  `// Generated by scripts/build-gen3-data.cjs from Pokemon Showdown's Gen 3 Dex.\nexport const GEN3_DATA = ${JSON.stringify(data)};\n`,
);

console.log(
  `Wrote ${outputPath} (${species.length} species, ${moves.length} moves, ${items.length} items, ${abilities.length} abilities)`,
);
